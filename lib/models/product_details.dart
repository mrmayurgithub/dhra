class ProductDetails {
  String url;
  String modelName;
  String brand;
  int storage;
  int ram;
  String os;
  String color;
  double screenSize;
  String displayType;
  ProductDetails({
    required this.url,
    required this.modelName,
    required this.brand,
    required this.storage,
    required this.ram,
    required this.os,
    required this.color,
    required this.screenSize,
    required this.displayType,
  });
}
